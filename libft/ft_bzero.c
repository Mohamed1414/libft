#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	unsigned int	i;
	unsigned char	*a;

	i = 0;
	a = s;
	while (i < n && a[i])
	{
		a[i] = '\0';
		i++;
	}
}
